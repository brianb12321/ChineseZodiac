<h1>A Brief History on The Significance and Changes of PHP</h1>
<p>
    The PHP Hypertext PreProcessor (recursive acronym) is one of the most popular server-side scripting languages for web-development.<br/>
    Started as a tool for his website, Rasmus Lerdorf began developing the scripting language to what we now know as PHP.<br/>
    Originally developed with C paradigms, PHP still resemble this tidbit of history. Your favorite C functions like printf, sscanf, fopen, and strcmp, are usable in PHP.<br/>
</p>
<p>
    PHP was not originally intended to be a full language, but the language grew regardless. As a side-effect, there were no standard naming conventions for functions.<br/>
    For example, str_replace vs strcmp. One uses an underscore while another does not. PHP 3 would eventually be released in 1998.<br/>
</p>
<p>
    As PHP became more popular and programming paradigms were adopted, PHP needed to adapt. Starting in PHP 5, object oriented programming and the database API (PDO) were built.<br/>
    Additional performance boost was also added. Probably the most famous version of PHP is version 7 (2014). The Zend engine was completely re-written with the capability for Just In Time compilation in the future.<br/>
    WordPress, who used PHP around this period, was the staple CMS for many bloggers and webmasters. The new performance boosts with the new Zend engine would give WordPress nearly 100% speed boost.<br/>
    Additionally, legacy code was removed, some major bugs were squashed, and added some new language features like (return type declarations).<br/>
    PHP 8 included JIT compilation, weak maps, and other language changes.<br/>
</p>
<p>
    So, PHP has an interesting history. So what? PHP has ran numerous famous projects and websites throughout its history. Some of the following projects were powered on PHP:
</p>
<ul>
    <li>WorPress</li>
    <li>Moodle</li>
    <li>Joomla</li>
    <li>Drupal</li>
    <li>Wikipedia using MediaWiki</li>
</ul>
