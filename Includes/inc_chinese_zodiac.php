<h1>What Is The Chinese Zodiac?</h1>

<p>
The Chinese Zodiac is an ancient belief in which every year is represented by one of the twelve creatures of Chinese tradition-<br/>
-Rat, Ox, Tiger, Rabbit, Dragon, Snake, Horse, Goat, Monkey, Rooster, Dog, and Pig.<br/>
Each creature has a unique personality and special trait. In Chinese beliefs, these traits determine individuals personality and future.<br/>
For example, a person born in the year of the ox may exhibit power or "better luck" compared to last year.<br/>
Incidentally, the year 2021 is the ox.<br/>
</p>

<p>
The Chinese Zodiac has been around since the Qin dynasty. This would make sense as many people believed in what the stars tell them.<br/>
Some people have speculated the twelve creatures was a simplified version of the twenty-eight constellations.<br/>
Other people believed the Zodiac was inspired from animal worship.<br/>
</p>

<p>
The month and year of birth is critical in determining your Zodiac creature. However, a year in the Chinese lunar calendar is different than the year<br/>
in our Gregorian calendar--used in the United States.<br/>
The Chinese Lunar Calendar is based on symbolic imagery--a common feature in Chinese tradition. In contrast, the Gregorian calendar is based on a 365 day rotation of Earth.<br/>
Every year starts on January 1.<br/>
The start of the year in the Lunar calendar is different every year--Chinese New Year.<br/>
</p>

